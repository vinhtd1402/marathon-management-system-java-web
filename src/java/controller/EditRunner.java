    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        int id = Integer.parseInt(request.getParameter("id"));
        AccountDAO dao = new AccountDAO();
        Account runner = dao.getAccountById(id);
        request.setAttribute("runner", runner);
        RequestDispatcher rd = request.getRequestDispatcher("views/staff/EditRunner.jsp");
        rd.forward(request, response);
    } 

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
          Account runner = new Account();
        runner.setuID(Integer.parseInt(request.getParameter("id")));
        runner.setFullName(request.getParameter("fullName"));
        runner.setGender(request.getParameter("gender"));
        runner.setUsername(request.getParameter("username"));
        runner.setPassword(request.getParameter("password"));
        runner.setAge(Integer.parseInt(request.getParameter("age")));
        runner.setHealthStatus(request.getParameter("healthStatus"));
        runner.setEmail(request.getParameter("email"));
        runner.setPhone(request.getParameter("phone"));
        runner.setRoleID(3);

        AccountDAO dao = new AccountDAO();
        dao.updateAccount(runner);
        response.sendRedirect("accountRunner");
    }

